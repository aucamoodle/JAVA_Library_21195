    private void photoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_photoBtnActionPerformed
        // TODO add your handling code here:
        
        JFileChooser jc =new JFileChooser();
        int i = jc.showOpenDialog(this);
        if (i == JFileChooser.APPROVE_OPTION) {
            f=jc.getSelectedFile();
            BufferedImage img = null;
            try {
                img = ImageIO.read(f);
            } catch (IOException ex) {
                Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
            }
            Image dimg = img.getScaledInstance(Photo.getWidth(), Photo.getHeight(), Image.SCALE_AREA_AVERAGING);
            ImageIcon ic = new ImageIcon(dimg);
            Photo.setIcon(ic);
        }
    }//GEN-LAST:event_photoBtnActionPerformed

    private void AddClientBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddClientBtnActionPerformed
        // TODO add your handling code here:
        
        Client c=new Client();
        c.setEmailAddress(emailtxt.getText());
        c.setPhoneNumber(phoneTxt.getText());
        c.setFname(fnameTxt.getText());
        c.setLname(lnametxt.getText());
        if(CatCombo.getSelectedItem().toString().equalsIgnoreCase("student")){
        c.setCat(CCategory.STUDENT.getCategory().toString());
        }else if(CatCombo.getSelectedItem().toString().equalsIgnoreCase("staff")){
          c.setCat(CCategory.STAFF.getCategory().toString());
        }else{
        
        c.setCat(CCategory.EXTERNAL.getCategory().toString());
        }
        c.setPhoto(f);
        new Controller().insertClient(c);
        JOptionPane.showMessageDialog(null,"inserted");
    }//GEN-LAST:event_AddClientBtnActionPerformed
