public void populateTable(){
List<Client> ls= new Controller().AllClients();
dm=(DefaultTableModel) jTable1.getModel();
    for (Iterator<Client> iterator = ls.iterator(); iterator.hasNext();) {
        Client next = iterator.next();
        dm.addRow(new Object[]{
        next.getClientid(),next.getFname(),
            next.getLname(),next.getEmailAddress(),
            next.getCat()
        
        });
    }

}
public void populateCombo(){
List<String> ls=new ArrayList<>();
ls.add("STUDENT");
ls.add("STAFF");
ls.add("EXTERNAL");
dmc=(DefaultComboBoxModel) CatCombo.getModel();
dmc.removeAllElements();
    for (Iterator<String> iterator = ls.iterator(); iterator.hasNext();) {
        String next = iterator.next();
        dmc.addElement(next);
    }
}
